-- Funcion de suma
   suma x y = x + y
-- Funcion de resta
   resta x y = x - y
-- Funcion media aritmetica de tres numeros
   mediaTres x y z = (x + y + z)/3
-- Funcion doble de un numero
   doble x = 2*x
-- Funcion potencia de un numero
   potencia x = x * x
-- Funcion maximo de tres numeros
   maxTres x y z = max x (max y z)
-- Funcion minimo de tres numeros
   minTres x y z = min x (min y z)
-- Funcion verifica si tres numeros son iguales
   igualTres x y z = x == y && y == z
-- Funcion que verifica si tres numeros son diferentes
   difTres x y z = x/=y && x/=z && y/=z
   
   